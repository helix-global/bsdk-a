using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.IO;
using System.Xml.Serialization;

namespace BinaryStudio.ReportingServices.FormattingObjects
    {
    [Serializable]
    [XmlType(Namespace = "http://www.w3.org/1999/XSL/Format")]
    [XmlRoot(Namespace = "http://www.w3.org/1999/XSL/Format",ElementName="layout-master-set")]
    public class LayoutMasterSet
        {
        [XmlElement("page-sequence-master")] public PageSequenceMaster PageSequenceMaster { get;set; }
        [XmlElement("simple-page-master")] public SimplePageMaster SimplePageMaster { get;set; }
        public LayoutMasterSet()
            {
            PageSequenceMaster = new PageSequenceMaster();
            SimplePageMaster = new SimplePageMaster();
            }

        #region P:Serializer:XmlSerializer
        private static XmlSerializer serializer;
        public static XmlSerializer Serializer { get {
            serializer = serializer ?? new XmlSerializer(typeof(LayoutMasterSet));
            return serializer;
            }}
        #endregion

        public override String Serialize()
            {
            using (var output = new MemoryStream()) {
                Serializer.Serialize(output, this);
                output.Seek(0, SeekOrigin.Begin);
                using (var reader = new StreamReader(output)) {
                    return reader.ReadToEnd();
                    }
                }
            }
        }
    }
